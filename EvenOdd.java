import java.util.Scanner;
public class EvenOdd {
	public static void main(String args[]) {
		
		Scanner input = new Scanner(System.in);
		int num;
		System.out.print("정수를 입력하세요:");
		num = input.nextInt();
		
		if(num%2==0)
			System.out.print("양수입니다.");
		
		else
			System.out.print("음수입니다.");
	}
}
